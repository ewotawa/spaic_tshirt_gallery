var i;
for (i = 0; i < gallery.length; i++) { 
  // locate image for gallery item i
  var path = "./assets/gallery_images/" + gallery[i].image
  // locate section for gallery items
  var section = document.getElementById('gallery');
  
  // create div with class flex
  var div_flex = document.createElement('div');
  div_flex.setAttribute('class', 'flex');
  
  // create div with class gallery
  var div_gallery = document.createElement('div')
  div_gallery.setAttribute('class', 'gallery');
  
  // create a with various attributes
  var a = document.createElement('a');
  a.setAttribute('target', "_blank");
  a.setAttribute('href', "#");
  
  // create img
  var img = document.createElement('img');
  img.setAttribute('src', path);
  img.setAttribute('alt', 'AI-generated image');
  
  // create div with class desc
  
  var div_ballot = document.createElement('div');
  div_ballot.setAttribute('class', 'desc');
  var ballot = document.createTextNode(gallery[i].id);
  
  // create div with class desc
  var div_student = document.createElement('div');
  div_student.setAttribute('class', 'desc');
  var student = document.createTextNode(gallery[i].student);
  
  // append flex div to section div
  section.append(div_flex);
  // append gallery div to flex div
  div_flex.append(div_gallery);
  // append a tag to gallery div
  div_gallery.append(a);
  // append img tag to a tag
  a.append(img);
  // append ballot text to ballot div
  div_ballot.appendChild(ballot).textContent;
  // append ballot div to gallery div
  div_gallery.append(div_ballot);
  
  // append student text to student div
  div_student.appendChild(student).textContent;
  // append student div to gallery div
  div_gallery.append(div_student);
  
  // for those gallery items with descriptions, create div with class desc
  if(gallery[i].description) {
    var div_desc = document.createElement('div')
    div_desc.setAttribute('class', 'desc');
    var desc = document.createTextNode(gallery[i].description);
    div_desc.appendChild(desc).textContent;
    div_gallery.appendChild(div_desc);
  }
}